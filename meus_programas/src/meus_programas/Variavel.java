package meus_programas;

public class Variavel {
	public static void main (String [] args) {
		
		
		// VARIÁVEIS:
		// tipo (String, int, boolean)
		// nome (nome, idade, casado)
		// valor ("Rodrigo", 37, false
		// = operador de atribuição
		// ; finalizar a instrução
		
		String nome = "Rodrigo";
		int idade = 37;
		boolean casado = false;
		
		/* Variáveis do mesmo tipo podemos declarar várias separadas por vírgula.EX:
		
			String nome, segundoNome, ultimoNome;
			
			Inicializando as variáveis:
			
			nome = "Rodrigo";
			segundoNome = "Fernando";
			ultimoNome = "Geremias";
		*/
		System.out.println (nome); // imprimir valor da variável na tela.
		System.out.println (idade);
		System.out.println (casado);
		
		}

}
